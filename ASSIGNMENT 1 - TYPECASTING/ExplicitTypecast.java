public class ExplicitTypecast {
    public static void main(String[] args) {
    //Assign a double value to an int variable using explicit typecasting and print both values.
    double doublevalue= 45.65;
    int intvalue = (int) doublevalue;
    System.out.println("Doublevariable : "+doublevalue);
    System.out.println("intvalue : "+intvalue);
    //Assign an int value to a byte variable using explicit typecasting and print both values.
    int intvariable = 123;
    byte bytevalue = (byte) intvariable;
    System.out.println("intvariable : "+intvariable);
    System.out.println("bytevalue : "+bytevalue);
    }
}
